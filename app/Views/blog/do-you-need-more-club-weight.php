        <h2 style="color: var(--deep-green); font-size: 2rem; margin: 3rem 0 1.5rem 0; border-bottom: 2px solid var(--light); padding-bottom: 0.5rem;">Nate and Ellen: Two Sides of the Weight Story</h2>
        
        <p>By the time Nate came in, his tempo had a tremor in it. Not nerves—fatigue. He'd been swinging a set that looked fast on paper: feather‑light shafts, heads shaved of every gram, grips that felt like air. "I thought lighter meant easier," he said, and then he showed me the flight: a thin that climbed and stalled, a block that held on to the right edge like a ledge, then one perfect ball that only showed up when he swung harder than he wanted to.</p>

        <p>Our shop is small and quiet on purpose. When you set a club on the bench, the room tells the truth back. We measured his irons and driver—static weight (what a scale sees) and swing weight (how heavy the club feels in motion, the balance between head and hands). Nate's totals were low end; his swing weights, too. The numbers only confirmed what his hands already knew: he couldn't feel the head long enough to time it.</p>

        <p>We didn't start with a lecture. We started with two grams.</p>

        <p>A sliver of head weight on the 7‑iron. Then another. Two grams is almost nothing to the eye and everything to your sense of the clubhead; about +1 swing‑weight point for each ~2 grams at the head. He hit three balls. The first didn't look miraculous. It looked calmer. The face arrived like it had an invitation. The thin miss came off the bottom groove but didn't surrender its carry. His shoulders dropped. "I can feel it," he said, which is another way of saying, "I can trust it."</p>

        <p>We added a touch more to the long iron, and this time feathered a small counterweight in the butt of the grip to keep the head presence without letting the downswing feel bossy. (At the hands, ~5 grams tends to lower measured swing weight about a point and can smooth over‑quick transitions.) He didn't swing faster. He didn't have to. A better timing showed up—his only job was to keep breathing.</p>

        <p>Here's the unromantic truth that saves a lot of golfers: total weight and swing weight are different levers.</p>

        <ul>
            <li><strong>Total (static) weight</strong> is the mass you carry for 18 holes. Too heavy and your body tires, the face arrives late, and the end of a round feels like a chore.</li>
            <li><strong>Swing weight</strong> is the balance your hands feel. Add a little at the head and the head feels present; add at the butt and the system feels steadier. Change length and the lever changes (about +3 swing‑weight points for every +½ inch, all else equal).</li>
        </ul>

        <p>A few grams in the right place can make your swing feel like yours again.</p>

        <p>But it isn't always about "more." A week later, Ellen came in with the opposite problem. Her set was honest, maybe a touch too honest—heavy shafts she loved in her 40s that now made the back nine feel longer than the front. Her best shot still had that round, complete sound. But the average ball fell a club short, and her misses were late because she was tired.</p>

        <p>We respected what her hands liked—a head she could sense—and we trimmed the total weight responsibly. A lighter shaft, not the lightest; keep the swing weight in a window that preserved head feel (remember, shaft weight changes can nudge swing weight—on the order of ~1 point per ~9 grams depending on balance). We left the head weight alone on the scoring irons and gave back a touch of head feel in the long irons so she didn't have to swing harder to hear the same story. On the monitor, nothing exploded. It just added up: the end of the round looked like the beginning, and the late face disappeared with her fatigue.</p>

        <h2>If you're trying to choose a direction this week, start with what your body and ball flight keep telling you:</h2>

        <h3>Signs you may need a bit more effective head feel (or total weight):</h3>
        <ul>
            <li>Your transition feels jittery; the club wants to race from the top.</li>
            <li>You "lose" the head—can't sense it long enough to time the release.</li>
            <li>Misses balloon or float; toe‑side smudges show up on face tape.</li>
            <li>Your best shot only appears when you swing harder than you want to.</li>
        </ul>

        <h3>Signs you may need a little less total weight (or a steadier balance):</h3>
        <ul>
            <li>You're fine for 9 holes and fading by 15.</li>
            <li>You feel late—face open, low bullets or weak blocks when you're tired.</li>
            <li>The club feels like effort, not rhythm; hands squeeze more as the day goes on.</li>
            <li>Recovery swings (punches, controlled shots) are better than full ones.</li>
        </ul>

        <h2>Ways we tune it, gently:</h2>
        <ul>
            <li><strong>Head weight:</strong> lead slivers, tip weights, hot‑melt in metalwoods—about +1 swing‑weight point per ~2 g at the head.</li>
            <li><strong>Butt/counterweight:</strong> a small weight under the grip—about −1 swing‑weight point per ~5 g—to calm quick transitions without killing head feel.</li>
            <li><strong>Length:</strong> a half‑inch change is a big lever; roughly ±3 swing‑weight points plus changes to lie/strike—use sparingly and test.</li>
            <li><strong>Shaft weight & profile:</strong> small jumps up or down to match your energy level and tempo; consider how the shaft balances (counterbalanced models can keep head feel while trimming total weight).</li>
        </ul>

        <p>None of this is about being "strong enough" or "fast enough." It's about peace. The right weight picture lets you make the motion you already own—without bracing, without forcing, without bargaining with your confidence every swing.</p>

        <p>When Nate came back after his round, he didn't bring numbers. He brought a feeling. "My misses didn't scare me," he said. That's what a few grams can do when they're in the right place: take the drama out of ordinary shots so your courage is free for the ones that deserve it.</p>

        <p>And Ellen? She sent a message I keep on the wall: "The 17th no longer feels like a hill." That's weight, telling the truth where it matters—at the end of the day, with the match in the balance, with your body asking whether you chose wisely.</p>

        <p>If you're reading this because you're tired of guessing, bring us the swing you have. We'll measure static weight and swing weight on the bench, put a mid‑iron and a driver on the mat, and test in grams, not guesses—two here, maybe five there—until your tempo and your strike stop arguing. Maybe you need more. Maybe less. What you need is yours.</p>

