            <p style="font-size: 1.2rem; margin-bottom: 2rem; color: #555; font-style: italic;">
                He came in with two numbers written on a scorecard: 97 and 82. "Driver speed, six‑iron speed," he said, a little sheepish, like he'd been caught peeking at the answers. "So I'm Stiff, right?" He was hoping the math would save him. It almost could.
            </p>

            <h2 style="color: var(--deep-green); font-size: 2rem; margin: 3rem 0 1.5rem 0; border-bottom: 2px solid var(--light); padding-bottom: 0.5rem;">The Numbers That Don't Lie</h2>
            
            <p style="margin-bottom: 2rem;">
                We set the driver behind a foam tee in the quiet of the workshop—the hum of the monitor, the faint solvent smell, the small clink of ferrules in a tray. He had that unhurried backswing some golfers are lucky enough to be born with. Not slow, exactly—smooth. His first three drives drew a soft question mark in the air: one perfect, one hanging right, one with a little spinny stall at the top. The numbers were honest. The story wasn't finished.
            </p>

            <p style="margin-bottom: 2rem;">
                Flex is not a badge. It's a match. It's how the shaft bends under your speed and then returns under your tempo—that tiny moment at the top when your swing changes direction and the club decides whether to come with you or fight you. Speed pushes the choice one way. Tempo pushes just as hard the other. When both forces agree, the face finds the ball on time and the ball flight looks like something you can live with on the 16th tee.
            </p>

            <p style="margin-bottom: 2rem;">
                He set the six‑iron down next. We kept the head the same and bracket‑tested two shafts: one a true Stiff, firm in the tip; the other a Regular that was a hair heavier than most people expect, designed for smooth transitions. The Stiff gave him two postcards and three apologies. The heavier Regular gave him fewer headlines and far fewer arguments. That's what the right flex does: it makes your good swing repeat without demanding that you become someone else.
            </p>

