            <h2 style="color: var(--deep-green); font-size: 2rem; margin: 3rem 0 1.5rem 0; border-bottom: 2px solid var(--light); padding-bottom: 0.5rem;">The Speed Charts</h2>
            
            <p style="margin-bottom: 2rem;">
                Below are the typical windows—driver and six‑iron—so you have a map before you walk in. They're guidelines, not laws. Brands label differently; profiles vary; your tempo can push you up or down a rung. Read these like road signs. Then test.
            </p>

            <h3 style="color: var(--deep-green); font-size: 1.5rem; margin: 2rem 0 1rem 0;">Driver clubhead speed → typical flex</h3>
            <ul style="margin-bottom: 2rem; padding-left: 2rem;">
                <li style="margin-bottom: 1rem;">Under 75 mph → L (Ladies)</li>
                <li style="margin-bottom: 1rem;">75–85 mph → A/M (Senior/Amateur)</li>
                <li style="margin-bottom: 1rem;">85–95 mph → R (Regular)</li>
                <li style="margin-bottom: 1rem;">95–110 mph → S (Stiff)</li>
                <li style="margin-bottom: 1rem;">110+ mph → X (Extra Stiff)</li>
                <li style="margin-bottom: 1rem;">TX (Tour‑Extra) → only when very high speed meets a decisively aggressive transition and even X feels like it's folding on you</li>
            </ul>

            <h3 style="color: var(--deep-green); font-size: 1.5rem; margin: 2rem 0 1rem 0;">6‑iron clubhead speed → rough cues</h3>
            <ul style="margin-bottom: 2rem; padding-left: 2rem;">
                <li style="margin-bottom: 1rem;">Under 60 mph → L</li>
                <li style="margin-bottom: 1rem;">60–70 mph → A/M</li>
                <li style="margin-bottom: 1rem;">70–80 mph → R</li>
                <li style="margin-bottom: 1rem;">80–90 mph → S</li>
                <li style="margin-bottom: 1rem;">90+ mph → X (consider TX only if your transition truly mauls the shaft)</li>
            </ul>

            <p style="margin-bottom: 2rem;">
                Why list both? Because the driver tells us how fast you can move when the club is long and the ball is teed, while the six‑iron tells us how your speed and delivery live when the club is shorter and the strike has to be clean. If the two windows disagree, tempo is usually the tiebreaker.
            </p>

            <h2 style="color: var(--deep-green); font-size: 2rem; margin: 3rem 0 1.5rem 0; border-bottom: 2px solid var(--light); padding-bottom: 0.5rem;">Beyond the Numbers: What Else Matters</h2>
            
            <ul style="margin-bottom: 2rem; padding-left: 2rem;">
                <li style="margin-bottom: 1rem;"><strong>Labels aren't standardized.</strong> One brand's "Stiff" can feel like another brand's firm Regular. Feel the shaft bend and return in your hands.</li>
                
                <li style="margin-bottom: 1rem;"><strong>Weight matters as much as flex.</strong> A 65‑gram Stiff with a friendly mid section may be easier than an 80‑gram Regular with a locked tip. Get weight right first; flex will make sense after.</li>
                
                <li style="margin-bottom: 1rem;"><strong>Profile matters.</strong> "Where it bends" (butt/mid/tip) changes how the shaft loads for you. Smooth transitions often love mid‑soft, tip‑stable designs; snap‑and‑go transitions often need firmer tips so the face doesn't show up late.</li>
                
                <li style="margin-bottom: 1rem;"><strong>TX isn't a trophy.</strong> It's a tool for a very small slice of players. If you aren't already overpowering an X and timing it easily, TX will cost you more than it gives.</li>
            </ul>

            <h2 style="color: var(--deep-green); font-size: 2rem; margin: 3rem 0 1.5rem 0; border-bottom: 2px solid var(--light); padding-bottom: 0.5rem;">What the Wrong Flex Feels Like</h2>

            <p style="margin-bottom: 2rem;">
                <strong>Too soft:</strong> the head arrives early; right‑handers see left‑starts that keep turning; flight climbs and stalls; under pressure the timing turns whippy.
            </p>
            
            <p style="margin-bottom: 2rem;">
                <strong>Too stiff:</strong> the club won't load; flight flattens and leans right; you swing harder just to wake it up; impact feels boardy, like the shaft didn't want to help.
            </p>

            <p style="margin-bottom: 2rem; background: #f8f9fa; padding: 1.5rem; border-left: 4px solid var(--deep-green); border-radius: 4px;">
                <strong>And the right one?</strong> The right one makes you breathe out. You feel the shaft gather—elastic, not wobbly—and then nudge the face back to the ball on time. Your swing doesn't feel "different." It feels clear.
            </p>

            <h2 style="color: var(--deep-green); font-size: 2rem; margin: 3rem 0 1.5rem 0; border-bottom: 2px solid var(--light); padding-bottom: 0.5rem;">Testing at Home</h2>
            
            <p style="margin-bottom: 2rem;">
                If you're working on this at home, here's an honest way to test without turning the range into a laboratory:
            </p>

            <ul style="margin-bottom: 2rem; padding-left: 2rem;">
                <li style="margin-bottom: 1rem;"><strong>Bracket by speed.</strong> If your driver speed is ~97 mph and your six‑iron is ~82, test R‑heavy vs S‑friendly with the same head and length.</li>
                
                <li style="margin-bottom: 1rem;"><strong>Watch patterns, not heroes.</strong> Ten balls with each. Toss the two strangest. Which window is calmer? Which miss is kinder?</li>
                
                <li style="margin-bottom: 1rem;"><strong>Mind the six‑iron.</strong> If the six‑iron begs for Stiff but the driver is a wash, you likely need a more stable profile (firmer tip or a touch more weight) rather than jumping all the way to a boardy X in the driver.</li>
                
                <li style="margin-bottom: 1rem;"><strong>Don't chase spin with flex alone.</strong> Loft, face tech, and strike point drive spin as much as flex. Fix strike first.</li>
            </ul>

            <p style="margin-bottom: 2rem;">
                Back to our scorecard guy. With driver at 97 and six‑iron at 82, the chart says Stiff. His tempo said "don't over‑tighten." In the bay, the heavier Regular read his rhythm better: launch settled, spin stopped arguing, and the miss got boring (that's a compliment). We left his driver in a friendly Stiff profile—mid‑section that let him load, tip that didn't give up. The numbers matched the map. The feel made the map worth following.
            </p>

            <h2 style="color: var(--deep-green); font-size: 2rem; margin: 3rem 0 1.5rem 0; border-bottom: 2px solid var(--light); padding-bottom: 0.5rem;">Key Takeaways</h2>
            
            <ul style="margin-bottom: 2rem; padding-left: 2rem;">
                <li style="margin-bottom: 1rem;">You don't have to swing angrier to earn a stiffer shaft. If the club fits, it will meet you where you already live.</li>
                
                <li style="margin-bottom: 1rem;">If you're between sizes, fit the six‑iron first. It's the truth serum of the set. Get the iron right; then echo that feel up the bag.</li>
                
                <li style="margin-bottom: 1rem;">TX is a yes‑or‑no question. If you're wondering, the answer is no. If you're a TX golfer, you already know—you've been folding X without trying.</li>
                
                <li style="margin-bottom: 1rem;">The best shaft won't make you someone else. It will make you easier to repeat.</li>
            </ul>

            <p style="margin-bottom: 3rem;">
                He came back after a week and didn't brag about a single 320‑yard outlier. He told me his ten‑yard window looked the same all day. That's what the right flex by speed and tempo buys you: not fireworks, but fidelity.
            </p>

